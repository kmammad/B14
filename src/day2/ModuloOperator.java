package day2;

public class ModuloOperator {

    public static void main(String[] args) {

        System.out.println(10 % 3); //1 remainer after division
        System.out.println(15 % 4); //3
        System.out.println(10 % 2); //0
        System.out.println(3 % 7); //3 if the first number is smaller than second number, the remainder is always the value of the first number
        System.out.println(2 % 231); //2
        System.out.println(4 % 6); //4


        //modular operator is used to check if the number is odd or even number
        //if the result is 0 then it is even number
        // if the result is a number it is odd number


        System.out.println(123 % 10); //3

        // Any number modulo 10 -> getting the last digit

    }
}
